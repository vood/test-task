async function readInterviewPeople() {
  const files = await listFiles(path.join(process.cwd(), "data", "interviews"));
  const people: InterviewPerson[] = [];
  for (const absolute of files.filter((file) => file.endsWith(".md"))) {
    const content = await readFile(absolute, "utf8");
    const person = {
      name: requiredMeta(content, "Name", absolute),
      role: requiredMeta(content, "Role", absolute),
      department: requiredMeta(content, "Department", absolute),
      email: requiredMeta(content, "Email", absolute),
      source: path.relative(process.cwd(), absolute),
    };
    people.push(person);
  }
  return people;
}

async function listFiles(dir: string): Promise<string[]> {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const absolute = path.join(dir, entry.name);
      return entry.isDirectory() ? listFiles(absolute) : [absolute];
    }),
  );
  return files.flat().sort();
}

function requiredMeta(content: string, field: string, source: string) {
  const match = content.match(new RegExp(`^\\s*-\\s*\\*\\*${field}:\\*\\*\\s*(.+)$`, "im"));
  if (!match?.[1]) {
    throw new Error(`Missing ${field} in ${source}`);
  }
  return match[1].trim();
}

function reportsTo(name: string) {
  return {
    "Sara Wijaya": "Daniel Tan",
    "Sarah Ng": "Priya Raman",
  }[name];
}

function distinctFrom(name: string) {
  const pairs: Record<string, string> = {
    "Maria Silva": "Maria Santos",
    "Maria Santos": "Maria Silva",
    "Aisha Yusof": "Aisha Mahmud",
    "Aisha Mahmud": "Aisha Yusof",
    "Sara Wijaya": "Sarah Ng",
    "Sarah Ng": "Sara Wijaya",
  };
  return pairs[name] ? [`entity.person.${slug(pairs[name])}`] : undefined;
}

function slug(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");
}

function validateSourceRefs(records: JsonRecord[]) {
  for (const record of records) {
    for (const sourceRef of record.source_refs) {
      if (!existsSync(path.join(process.cwd(), sourceRef))) {
        throw new Error(`${record.id} references missing source ${sourceRef}`);
      }
    }
  }
}

async function writeJsonl(fileName: string, records: JsonRecord[]) {
  const content = records.map((record) => JSON.stringify(removeUndefined(record))).join("\n") + "\n";
  await writeFile(path.join(normalizedDir, fileName), content, "utf8");
}

function removeUndefined(record: JsonRecord) {
  return Object.fromEntries(Object.entries(record).filter(([, value]) => value !== undefined));
}

