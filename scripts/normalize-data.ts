import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";

type JsonRecord = Record<string, unknown> & {
  id: string;
  source_refs: string[];
};

type InterviewPerson = {
  name: string;
  role: string;
  department: string;
  email: string;
  source: string;
};

const normalizedDir = path.join(process.cwd(), "data", "normalized");

async function main() {
  const people = await readInterviewPeople();
  const entityRecords = [...seedEntityRecords(), ...personEntityRecords(people)];
  const factRecords = seedFactRecords();
  const causalRecords = seedCausalRecords();

  validateSourceRefs([...entityRecords, ...factRecords, ...causalRecords]);

  await mkdir(normalizedDir, { recursive: true });
  await writeJsonl("entities.jsonl", entityRecords);
  await writeJsonl("facts.jsonl", factRecords);
  await writeJsonl("causal_chains.jsonl", causalRecords);
  await writeFile(
    path.join(normalizedDir, "README.md"),
    [
      "# Normalized Knowledge Layer",
      "",
      "This directory is generated by `npm run normalize`.",
      "",
      "The generic assistant should prefer these records for entity, alias, relationship, freshness, conflict, and causality resolution, then use original files in `data/` as provenance and fallback evidence.",
      "",
      "Each JSONL record includes `source_refs` pointing to original records that back the normalized interpretation. Normalized files guide reasoning but should not be cited as user-facing sources.",
      "",
    ].join("\n"),
    "utf8",
  );

  console.log(`Wrote ${entityRecords.length} entities, ${factRecords.length} facts, ${causalRecords.length} causal chains.`);
}

